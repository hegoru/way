# Numerics is a base class of other specific number classes.
# It consists of Integer, Float, Rational classes.
#
# Integer
# 
puts 110.class # Way of declaring Integer
puts -110.class # Way of declaring negative Integer

# Representations of Integer
# There are 
# All the Integers can be represented in one of this forms.
# 
puts 0x00006e # Hexadecimal
puts 00156 # Octal
puts 0b01101110 # Binary

# Float
#
puts (110.10).class # Way of declaring Float
puts (1.2e-3).class # Another way of declaring a Float

# Rational
#
puts (110.10r).class # Way of declaring Rational

# Characters
#
puts ?n # ASCII character for 'n'

puts ?\C-n # Control-n
puts ?\M-n # Meta-n
puts ?\M-\C-n # Meta-Control-n